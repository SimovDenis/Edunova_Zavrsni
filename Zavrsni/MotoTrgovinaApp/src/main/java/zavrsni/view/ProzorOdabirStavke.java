    @Override
    public void ucitaj() {
        DefaultListModel<Proizvod> m = new DefaultListModel<>();
        m.addAll(obradaProizvod.read());
        lstPodaci.setModel(m);
        lstPodaci.repaint();
    }

    @Override
    public void popuniModel() {
        var s = obrada.getEntitet();

        s.setRacun(prozorRacun.getObradaRacun().getEntitet());
        s.setProizvod(lstPodaci.getSelectedValue());
        try {
            s.setKolicina(Integer.valueOf(txtKolicina.getText()));
        } catch (NumberFormatException ex) {
            s.setKolicina(0);
        }

    }

